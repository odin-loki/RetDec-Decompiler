/**
 * @file src/eh_reconstruct/borland_eh.cpp
 * @brief Borland/Embarcadero C++ 32-bit exception handling reconstruction.
 *
 * ## Borland EH model (32-bit x86)
 *
 * Borland C++ Builder and Delphi/C++ Builder store C++ exception handling
 * using a Windows SEH-style linked list of exception frames on the stack,
 * but with proprietary frame structure and static handler tables.
 *
 * ### Function prolog pattern
 *
 * Every function that uses try/catch emits this prolog sequence:
 *
 *   PUSH <handler_addr>          ; 68 <imm32> — push the handler function VMA
 *   PUSH DWORD PTR FS:[0]        ; 64 FF 35 00 00 00 00 — push previous head
 *   MOV  DWORD PTR FS:[0], ESP   ; 64 89 25 00 00 00 00 — install this frame
 *
 * (Sometimes MOV replaces the last two with variations, but this 14-byte
 *  sequence is the canonical form generated by Borland C++ 5.x – XE.)
 *
 * The exception frame on the stack at the MOV instruction has this layout
 * (growing downward, so the first PUSH lands at the highest address):
 *
 *   ESP+8 : handler_addr       (from PUSH <handler_addr>)
 *   ESP+4 : prev_fs0           (from PUSH FS:[0])
 *   ESP+0 : saved_register     (optional; varies by compiler version)
 *
 * The Borland handler function (at handler_addr) is a shared dispatcher
 * (__ExceptInit or __BExcHandler).  The static handler data for the function
 * is passed as a second hidden argument or retrieved via a table indexed by
 * the function's exception handler frame pointer.
 *
 * ### Static handler table
 *
 * The handler function jumps into a per-function handler stub that references
 * a static TExceptRec table.  Each TExceptRec entry (16 bytes) describes one
 * try block:
 *
 *   struct TExceptRec {
 *     DWORD  tryStart;      // offset from function start to try body start
 *     DWORD  tryEnd;        // offset from function start to try body end
 *     DWORD  catchOffset;   // offset from function start to catch handler code
 *     DWORD  pTypeInfo;     // pointer to Borland TypeInfo (or 0 = catch-all)
 *   };
 *
 * A zero-filled entry (all fields == 0) terminates the table.
 *
 * ### Borland TypeInfo
 *
 * Borland's TypeInfo for C++ classes uses a layout that mirrors its Pascal
 * ancestry.  For exception catching, the relevant field is the class name,
 * stored as a Pascal ShortString (length-prefixed):
 *
 *   struct TTypeInfo {
 *     BYTE   kind;         // 7 = tkClass for C++ classes
 *     BYTE   nameLen;
 *     char   name[nameLen]; // class name, NOT NUL-terminated
 *     // ... more fields
 *   };
 *
 * ### Detection fallback
 *
 * If no prolog pattern is found (e.g., compiler optimised it away), we also
 * scan the .data section for arrays of TExceptRec-like structures that have
 * valid code VMAs in the tryStart/catchOffset fields, as a secondary heuristic.
 *
 * ### Callee-save register scanning
 *
 * Borland 32-bit typically saves EBX, ESI, EDI, EBP using PUSH instructions
 * in the prolog.  We detect these with a simple scan of the first 32 bytes of
 * each function for PUSH reg (0x50+reg) opcodes.
 */

